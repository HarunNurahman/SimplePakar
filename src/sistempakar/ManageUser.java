    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String tampilan = "yyyy-MM-dd";
        SimpleDateFormat format = new SimpleDateFormat(tampilan);
        FormAdmin fa = new FormAdmin();
        
        String id;
        String user = txtuser.getText();
        String pass = txtpass.getText();
        String lvl = ceklvl.getSelectedItem().toString();
        String nik = txtnik.getText();
        String nama = txtnama.getText();
        String lahir = txttmp.getText();
        String tgl = String.valueOf(format.format(tgllhr.getDate()));
        String alamat = txtalamat.getText();
        String telp = txthp.getText();
        
        if(action.equals("add")){
            try {
                Statement stmt = koneksi.createStatement();
                String query = "INSERT INTO login(id, username, password, level, nik, nama, tmplahir, tgllahir, alamat, telp)"
                        + "VALUES(null,'"+user+"','"+pass+"', '"+lvl+"','"+nik+"','"+nama+"','"+lahir+"','"+tgl+"','"+alamat+"','"+telp+"')";
                System.out.println(query);
                int berhasil = stmt.executeUpdate(query);
                if (berhasil == 1){
                    JOptionPane.showMessageDialog(null, "Data Berhasil Ditambah");
                } else {
                    JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan");
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan pada DB atau Query");
            }
            fa.ShowUser();
            setVisible(false);
        } else {
            lbljdl.setText("Edit Penyakit");
            try {
                Statement stmt = koneksi.createStatement();
                String query = "UPDATE login SET username='" + user + "', "
                        + "password='" + pass + "', level='" + lvl + "', "
                        + "nik='" + nik + "', nama='" + nama + "', tmplahir= '"+ lahir +"',"
                        + " tgllahir= '"+ tgl +"', alamat= '"+ alamat +"', telp= '"+ telp +"'"
                        + " WHERE username='" + user + "'";
                System.out.println(query);
                int berhasil = stmt.executeUpdate(query);
                if (berhasil == 1) {
                    JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
                } else {
                    JOptionPane.showMessageDialog(null, "Data Gagal Diubah");
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan pada Query");
            }
            fa.ShowData();
            setVisible(false);
        }
    }//GEN-LAST:event_btnsaveActionPerformed


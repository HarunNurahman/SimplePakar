    void ShowUser(String act, String id) {
        if(action.equals("edit")){
            try {
                Statement stmt = koneksi.createStatement();
                String query = "SELECT * FROM login WHERE id = '" +id+ "'";
                ResultSet rs = stmt.executeQuery(query);
                rs.first();
                //txtid.setText(rs.getString("id"));
                txtuser.setText(rs.getString("username"));
                txtpass.setText(rs.getString("password"));
                ceklvl.setSelectedItem("level");
                txtnik.setText(rs.getString("nik"));
                txtnama.setText(rs.getString("nama"));
                txttmp.setText(rs.getString("tmplahir"));
                tgllhr.setDateFormatString(rs.getString("tgllahir"));
                txtalamat.setText(rs.getString("alamat"));
                txthp.setText(rs.getString("telp"));
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Kesalahan DB atau Query");
            }
        }
    }

